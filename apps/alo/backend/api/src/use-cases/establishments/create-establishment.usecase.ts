/**
 * Create Establishment Use Case
 */

import { CreateUseCaseTemplate } from '@oxlayer/snippets/use-cases';
import { EstablishmentRepository } from '@/repositories/index.js';
import { EventBus } from '@oxlayer/capabilities-events';
import { EstablishmentEntity, EstablishmentCreatedEvent } from '@/domain/index.js';

export interface CreateEstablishmentInput {
  name: string;
  horarioFuncionamento: string;
  description: string;
  ownerId: number;
  image?: string;
  primaryColor?: string;
  secondaryColor?: string;
  lat?: number;
  long?: number;
  locationString?: string;
  maxDistanceDelivery?: number;
  establishmentTypeId?: number;
  website?: string;
  whatsapp?: string;
  instagram?: string;
  googleBusinessUrl?: string;
}

export interface CreateEstablishmentOutput {
  id: number;
  name: string;
  createdAt: Date;
}

export class CreateEstablishmentUseCase extends CreateUseCaseTemplate<
  CreateEstablishmentInput,
  EstablishmentEntity,
  Promise<CreateEstablishmentOutput>
> {
  constructor(
    private establishmentRepository: EstablishmentRepository,
    eventBus: EventBus,
    tracer?: unknown | null
  ) {
    super({
      generateId: () => 0, // ID will be generated by database
      createEntity: (data) => EstablishmentEntity.create(data),
      persistEntity: async (entity) => {
        return await establishmentRepository.create(entity);
      },
      publishEvent: async (event) => {
        try {
          await eventBus.emit(event);
        } catch (error) {
          console.warn('Failed to publish event:', error);
        }
      },
      toOutput: (entity) => ({
        id: entity.id,
        name: entity.name,
        createdAt: entity.createdAt,
      }),
      tracer,
    });
  }

  protected override createEvent(entity: EstablishmentEntity, _id: number): EstablishmentCreatedEvent {
    return new EstablishmentCreatedEvent(entity.id, {
      ownerId: entity.ownerId,
      name: entity.name,
    });
  }
}

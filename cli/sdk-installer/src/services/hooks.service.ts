/**
 * Post-Install Hooks System
 *
 * Allows SDK packages to run setup code after installation
 */

import { promises as fs } from 'fs';
import { join } from 'path';
import { existsSync } from 'fs';

/**
 * Post-install context provided to hooks
 */
export interface PostInstallContext {
  version: string;
  vendorDir: string;
  projectRoot: string;
  projectType: string;
  packageManager: string;
  capabilities: Record<string, unknown>;
}

/**
 * Post-install hook function signature
 */
export type PostInstallHook = (context: PostInstallContext) => Promise<void> | void;

/**
 * Hook result
 */
export interface HookResult {
  hookName: string;
  status: 'success' | 'warning' | 'error';
  message: string;
  output?: string;
}

/**
 * Hook definition in package.json
 */
export interface HookDefinition {
  file: string;
  run?: string;
}

/**
 * Execute post-install hooks for a given version
 */
export async function executeHooks(context: PostInstallContext): Promise<HookResult[]> {
  const results: HookResult[] = [];

  // Find all hook files
  const hooksDir = join(context.vendorDir, 'hooks');
  if (!existsSync(hooksDir)) {
    return results;
  }

  const hookFiles = await fs.readdir(hooksDir).filter(f => f.endsWith('.js') || f.endsWith('.mjs'));

  for (const hookFile of hookFiles) {
    const hookPath = join(hooksDir, hookFile);
    const hookName = hookFile.replace(/\.(js|mjs)$/, '');

    try {
      // Clear require cache to ensure fresh execution
      delete require.cache[require.resolve(hookPath)];

      // Load and execute hook
      const hook: PostInstallHook = (await import(hookPath)).default || (await import(hookPath)).postInstall;

      if (typeof hook === 'function') {
        await hook(context);
        results.push({
          hookName,
          status: 'success',
          message: 'Hook executed successfully',
        });
      } else {
        results.push({
          hookName,
          status: 'warning',
          message: 'No default export or postInstall function found',
        });
      }
    } catch (err) {
      results.push({
        hookName,
        status: 'error',
        message: err instanceof Error ? err.message : 'Unknown error',
      });
    }
  }

  return results;
}


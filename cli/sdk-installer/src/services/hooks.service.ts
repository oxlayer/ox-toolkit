/**
 * Built-in post-install hooks
 */

/**
 * Hook: Generate .env file with SDK configuration
 */
export async function generateEnvHook(context: PostInstallContext): Promise<void> {
  const envPath = join(context.projectRoot, '.env');

  // Check if .env exists
  if (!existsSync(envPath)) {
    const envContent = `
# OxLayer SDK Configuration
# Generated by SDK installer
OXLAYER_SDK_VERSION=${context.version}
OXLAYER_VENDOR_DIR=.capabilities-vendor/${context.version}
`;

    await fs.writeFile(envPath, envContent.trim());
  }
}

/**
 * Hook: Update tsconfig.json paths
 */
export async function updateTsconfigPathsHook(context: PostInstallContext): Promise<void> {
  const tsconfigPath = join(context.projectRoot, 'tsconfig.json');

  if (!existsSync(tsconfigPath)) {
    return;
  }

  const tsconfig = JSON.parse(await fs.readFile(tsconfigPath, 'utf-8'));

  // Add paths for vendor directory
  if (!tsconfig.compilerOptions) {
    tsconfig.compilerOptions = {};
  }

  if (!tsconfig.compilerOptions.paths) {
    tsconfig.compilerOptions.paths = {};
  }

  // Add path mapping for SDK packages
  const vendorPath = `.capabilities-vendor/${context.version}`;
  tsconfig.compilerOptions.paths['@oxlayer/*'] = [`${vendorPath}/*`];

  await fs.writeFile(tsconfigPath, JSON.stringify(tsconfig, null, 2));
}

/**
 * Hook: Print capability warnings
 */
export async function printCapabilityWarningsHook(context: PostInstallContext): Promise<void> {
  const { capabilities } = context;

  // Check for specific capability limits and warn if needed
  for (const [capability, limits] of Object.entries(capabilities)) {
    if (typeof limits === 'object' && limits !== null) {
      if ('maxRealms' in limits && (limits as any).maxRealms === 1) {
        console.warn(`⚠️  Warning: Limited to 1 realm for ${capability}`);
      }
      if ('maxStorageGb' in limits && (limits as any).maxStorageGb < 100) {
        console.warn(`⚠️  Warning: Limited to ${(limits as any).maxStorageGb}GB storage for ${capability}`);
      }
    }
  }
}

/**
 * Execute built-in hooks
 */
export async function executeBuiltinHooks(context: PostInstallContext): Promise<void> {
  await generateEnvHook(context);
  await updateTsconfigPathsHook(context);
  await printCapabilityWarningsHook(context);
}

export default {
  executeHooks,
  createHookTemplate,
  executeBuiltinHooks,
};
